@Composable
fun AppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colorScheme = if (darkTheme) darkScheme else lightScheme

    // Створюємо об'єкт шрифту з ресурсів KMP
    // val playfairFont = Font(Res.font.playfair_display)

    // Передаємо його у твою функцію getTypography з файлу Type.kt
    //val appTypography = remember {
    //   getTypography(playfairFont)
    // }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = androidx.compose.material3.Typography(), // Тимчасовий дефолт
        content = content
    )
}